export function normalizePathname(pathname) {
  if (!pathname || pathname === '/') return '/'
  const p = pathname.replace(/\/+$/, '') || '/'
  return p
}

export function getSeoForPath(pathname) {
  const key = normalizePathname(pathname)
  return SEO_BY_PATH[key] || FALLBACK
}

